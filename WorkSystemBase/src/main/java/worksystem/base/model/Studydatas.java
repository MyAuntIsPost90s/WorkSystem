package worksystem.base.model;

public class Studydatas {
    private Long studydataid;

    private String studydataurl;

    private String studydatadesc;

    public Long getStudydataid() {
        return studydataid;
    }

    public void setStudydataid(Long studydataid) {
        this.studydataid = studydataid;
    }

    public String getStudydataurl() {
        return studydataurl;
    }

    public void setStudydataurl(String studydataurl) {
        this.studydataurl = studydataurl;
    }

    public String getStudydatadesc() {
        return studydatadesc;
    }

    public void setStudydatadesc(String studydatadesc) {
        this.studydatadesc = studydatadesc;
    }
}